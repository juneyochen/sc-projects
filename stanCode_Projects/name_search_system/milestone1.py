import sys


def add_data_for_name(name_data, year, rank, name):
    # 先判定"名字"是否在 name_data裡面
    if name not in name_data:
        name_data[name] = {year: rank}  # 這個名字要存他的year跟rank進一個dictionary
    else:
        # 如果"名字"已在name_data裡面，只要加入當年的排名，但此時需要先判定year有沒有在dictionary裡面
        if year not in name_data[name]:  # 沒有的話，把當年的排名存進dictionary
            name_data[name][year] = rank
        else:
            # 如果名字在data裡面，且year也在dictionary裡面，需要更新名字的排名
            if int(name_data[name][year]) > int(rank):
                name_data[name][year] = rank


